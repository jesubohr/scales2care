const getResult = (gender, isSmoker, age, cholesterol, pressure) => {
  const smoker = isSmoker ? "FUMA" : "NOFUMA";
  if (age < 50) {
    age = "40";
  } else if (50 <= age && age < 54) {
    age = "50";
  } else if (55 <= age && age < 59) {
    age = "55";
  } else if (60 <= age && age < 64) {
    age = "60";
  } else if (65 <= age && age < 69) {
    age = "65";
  } else if (70 <= age) {
    age = "70";
  }

  if (cholesterol <= 155) {
    cholesterol = "4";
  } else if (156 <= cholesterol && cholesterol <= 200) {
    cholesterol = "5";
  } else if (201 <= cholesterol && cholesterol <= 232) {
    cholesterol = "6";
  } else if (cholesterol > 232) {
    cholesterol = "7";
  }

  if (pressure <= 120) {
    pressure = "120";
  } else if (121 <= pressure && pressure <= 140) {
    pressure = "140";
  } else if (141 <= pressure && pressure <= 160) {
    pressure = "160";
  } else if (pressure > 161) {
    pressure = "180";
  }

  return scale[String(gender)][smoker][age][cholesterol][pressure];
};
